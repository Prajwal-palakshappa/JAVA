//do while loop 
import java.util.Scanner;
public class loops6 {
    public static void main(String[] args) {
        System.out.println("plz enter the numbers:");
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int i=1;
        do{
            System.out.println("hello world");
            i++;
        }while(i<=n);
    }
    
}
